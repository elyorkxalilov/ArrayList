package Lesson_13.Icecream;

public class Retsep {

    private int milkNul;
    private int sugerNul;

    public Retsep(int milkNul, int sugerNul) {
        this.milkNul = milkNul;
        this.sugerNul = sugerNul;
    }

    public int getMilkNul() {
        return milkNul;
    }

    public int getSugerNul() {
        return sugerNul;
    }
}
